		<h3 class="text-center">What It Does</h3>
		<p>The website can be split into two sections; the crime features and the walking prediction feature.</p>
		<b>Crime Features</b>
		<p>The crime aspect of this website allows you to use your <a href="/results/">social media data</a> and <a href="/search/">search areas</a> for crimes that have in and around areas in specific months, giving a detailed breakdown of crimes. The website can retrieve all of your data from your Facebook profile - and give you a breakdown of safe and unsafe locations you have been tagged in.</p>
		<b>Walking Prediction</b>
		<p>Using either your social media locations, or your <a href="/prediction-test/">own custom locations</a> - you can use the walking prediction model to roughly predict where you might go next (based on the locations you selected).</p>
	</div>
	<div class="col-md-6">
		<h3 class="text-center">Abstract</h3>
		<p>The following Abstract is taken from the paper "<i><a href="#" onclick="alert('Paper coming soon');">The Development of a Walking Prediction Model using Social Media Data, in the field of Crime Analysis</a></i>"</p>
		<p>There are various methods and techniques available to predict locations based on previous locations. These techniques have been developed using spatial-temporal reasoning, however none of these techniques are currently available to the general public. This project has been an experiment into the use of a walking prediction model for the general public.</p>
		<p>For this experiment both a walking prediction model and a website have been produced. The website retrieves social media data from usersâ€™ Facebook profiles and uses this to predict where they may go next. This geo-location data is input in to the walking prediction model, and the predicted location is displayed to the user.</p>
		<p>In order to give the website perspective, the field of crime has been used to warn users of crime risks in areas. The aim of this project is to experiment the feasibility of walking prediction models using social media geo-location data.</p>
